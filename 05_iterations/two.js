// while and do-while loop

let index = 0;
while(index <= 10){
    // console.log(`Value of index is ${index}`);
    index += 2;
}

let myArr = ["flash" , "batman" , "superman"];
let ind = 0;
while(ind < myArr.length){
    // console.log(`array element is ${myArr[ind]}`);
    ind++;
}

// do-while loop (runs at least once regardless of the condition)
let score = 11;
do{
    // console.log(score);
    score++;
}while(score<=10);