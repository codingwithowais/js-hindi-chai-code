// switch case statements
const month = "march";
switch (month) {
    case "jan":
        console.log("Jan");
        break;
    case "feb":
        console.log("Feb");
        break;
    case "march":
        console.log("March");
        break;
    case "april":
        console.log("April");
        break;

    default:
        console.log("Default month");
        break;
}

